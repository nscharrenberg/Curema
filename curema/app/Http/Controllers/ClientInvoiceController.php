    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $invoice = Invoice::findOrfail($id);

        if($invoice->client != Auth::user()->client) {
            return abort(404);
        } else {
            $payTypesArray = array();
            foreach(unserialize($invoice->allowed_payment_types) as $payType) {
                $payTypesArray[] = PaymentType::findOrFail($payType);
            }

            return view('client.invoices.show', compact('invoice', 'payTypesArray'));
        }

    }

